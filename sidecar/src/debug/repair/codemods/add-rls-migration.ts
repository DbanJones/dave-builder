// add-rls-migration codemod. Generates a fresh Supabase migration
// file enabling RLS on the table the rls-missing detector flagged,
// plus a per-row owner policy stub the user must complete.
//
// Per source spec §E.3 row 11: this is the canonical Tier 1 fix for
// the Lovable-class incident. Mechanical because the SQL DDL is
// boilerplate; the only semantic decision is which column identifies
// the row's owner — and that one we leave to a `TODO: edit` comment
// rather than guess.
//
// Approach:
//  1. Parse the table name out of the defect's codeEvidence (which
//     the detector emits as `CREATE TABLE <name> (… <piiColumns> …)`).
//  2. Find the next migration number by scanning supabase/migrations/
//     for files matching `^\d{4}_*.sql` and picking max+1.
//  3. Write a new file `supabase/migrations/<NNNN>_enable_rls_<table>.sql`
//     with ALTER TABLE … ENABLE RLS + a CREATE POLICY stub the user
//     completes.

import * as fs from "node:fs/promises";
import * as path from "node:path";

import type { Defect } from "../../../schema/defects.js";

import type { CodemodResult } from "../types.js";

const MIGRATIONS_DIR = path.join("supabase", "migrations");

export interface AddRlsMigrationInput {
  defect: Pick<Defect, "ruleId" | "file" | "lineStart" | "codeEvidence">;
  projectPath: string;
}

export function handlesRuleId(ruleId: string): boolean {
  return ruleId === "rls-missing/no-rls-on-pii-table";
}

export async function applyAddRlsMigration(
  input: AddRlsMigrationInput
): Promise<CodemodResult> {
  if (!handlesRuleId(input.defect.ruleId)) {
    return {
      kind: "skipped",
      message: `add-rls-migration: rule ${input.defect.ruleId} not handled`,
    };
  }

  const tableName = parseTableName(input.defect.codeEvidence);
  if (!tableName) {
    return {
      kind: "error",
      message: `add-rls-migration: could not extract table name from codeEvidence "${input.defect.codeEvidence}"`,
    };
  }

  const migrationsAbs = path.join(input.projectPath, MIGRATIONS_DIR);
  let nextNumber: string;
  try {
    nextNumber = await nextMigrationNumber(migrationsAbs);
  } catch (e) {
    return {
      kind: "error",
      message: `add-rls-migration: could not enumerate migrations: ${
        e instanceof Error ? e.message : String(e)
      }`,
    };
  }

  const fileName = `${nextNumber}_enable_rls_${tableName}.sql`;
  const fileRel = path.join(MIGRATIONS_DIR, fileName).split(path.sep).join("/");
  const fileAbs = path.join(migrationsAbs, fileName);
  const content = renderMigration(tableName);
  await fs.mkdir(migrationsAbs, { recursive: true });
  await fs.writeFile(fileAbs, content, "utf-8");

  return {
    kind: "applied",
    files: [fileRel],
    message:
      `Added migration ${fileName} that enables row-level security on "${tableName}" ` +
      `and seeds an owner-row policy. Open the file and replace the TODO ` +
      `column name with the column that identifies the row's owner ` +
      `(typically user_id or id depending on the table), then run ` +
      `\`supabase db push\` (or the equivalent).`,
    fixTier: 1,
  };
}

/**
 * Pull the table name out of strings shaped like
 * `CREATE TABLE users (… email, password_hash …)` (the format the
 * rls-missing detector emits in codeEvidence).
 */
export function parseTableName(codeEvidence: string): string | null {
  const m = /CREATE\s+TABLE\s+"?(\w+)"?/i.exec(codeEvidence);
  return m?.[1] ?? null;
}

/**
 * Return the next 4-digit migration prefix. Scans existing files,
 * extracts the leading number from each, returns max+1 padded to 4
 * digits. When no migrations exist, returns "0001".
 */
export async function nextMigrationNumber(migrationsDir: string): Promise<string> {
  let entries: string[];
  try {
    entries = await fs.readdir(migrationsDir);
  } catch {
    return "0001";
  }
  let max = 0;
  for (const name of entries) {
    const match = /^(\d{4,})_/.exec(name);
    if (!match) continue;
    const n = Number(match[1]);
    if (Number.isFinite(n) && n > max) max = n;
  }
  return String(max + 1).padStart(4, "0");
}

function renderMigration(tableName: string): string {
  return `-- Generated by the Builder's debug repair engine.
-- TODO before running: replace the OWNER_COLUMN placeholder with the
-- column that identifies the row's owner (typically user_id, or id on
-- the users table itself).

ALTER TABLE ${tableName} ENABLE ROW LEVEL SECURITY;

CREATE POLICY ${tableName}_owner_select ON ${tableName}
  FOR SELECT USING (auth.uid() = OWNER_COLUMN);

CREATE POLICY ${tableName}_owner_modify ON ${tableName}
  FOR ALL USING (auth.uid() = OWNER_COLUMN)
  WITH CHECK (auth.uid() = OWNER_COLUMN);
`;
}
